public class District {
    private String name;
    private double square;
    private City city;

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public double getSquare() {
        return square;
    }

    public void setSquare(final double square) {
        this.square = square;
    }

    public City getCity() {
        return city;
    }

    public void setCity(final City city) {
        this.city = city;
    }

}
